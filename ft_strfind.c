#include "libft.h"

int		ft_strfind(const char *s, int c)
{
	int		i;

	i = 0;
	if (!(s) || !(c))
		return (-1);
	while (s[i])
	{
		if (s[i] == (unsigned char)c)
			return (1);
		i++;
	}
	return (0);
}
