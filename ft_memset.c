#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*mem;
	size_t	i;

	i = 0;
	mem = b;
	while (i < len)
	{
		mem[i] = c;
		i++;
	}
	return (mem);
}
